# Ternary equivalent example in Python

# Traditional if-else statement
x = 10
if x > 5:
    result = "Greater"
else:
    result = "Smaller or Equal"


print("Normal if-else statement:", result)

# Ternary operator equ)ivalent
result = "Greater" if x > 5 else "Smaller or Equal"

print("Ternary equivalent:", result)	
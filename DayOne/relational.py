# Examples of relational operators in Python

a = 10
b = 20

# Equal to
print(f"{a} == {b} : {a == b}")

# Not equal to
print(f"{a} != {b} : {a != b}")

# Greater than
print(f"{a} > {b} : {a > b}")

# Less than
print(f"{a} < {b} : {a < b}")

# Greater than or equal to
print(f"{a} >= {b} : {a >= b}")

# Less than or equal to
print(f"{a} <= {b} : {a <= b}")
# Logical operators in Python

a = True
b = False

# AND operator
result_and = a and b
print(f"{a} AND {b} = {result_and}")

# OR operator
result_or = a or b
print(f"{a} OR {b} = {result_or}")

# NOT operator
result_not_a = not a
result_not_b = not b
print(f"NOT {a} = {result_not_a}")
print(f"NOT {b} = {result_not_b}")